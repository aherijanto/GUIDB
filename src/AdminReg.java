    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(!txtID.getText().isBlank()){
            String s = txtID.getText();
            String n = txtName.getText();
            String pwd = txtPswd.getText();
            String p = txtPhone.getText();
            try {
                ListAdmin.clearSelection();
                SubmitData(s,n,pwd,p);
                ShowAvailableAdmin();
                clearText();
            } catch (IOException ex) {
                Logger.getLogger(AdminReg.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            ShowError("Please Fill Blank Field..");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

