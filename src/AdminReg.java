    private void SubmitData(String s, String n, String pwd,String p) throws IOException{
        AdminModel _admin = new AdminModel(s,n,pwd,p);

            String _userData = _admin.ConvertToJSON();
            ConnectURI uriBuilder = new ConnectURI();
            URL inetAddress = uriBuilder.buildURL("http://localhost/register_admin.php");// SESUAIKAN DENGAN ADDRESS
            //uriBuilder.curlMe("http://localhost/register_admin.php",_userData);

            uriBuilder.postJSON(inetAddress, _userData);
    }
    /**
     * @param args the command line arguments
     */
    public  void ShowAvailableAdmin() throws IOException{
        ConnectURI uriBuilder = new ConnectURI();
        URL inetAddress = uriBuilder.buildURL("http://localhost/get_admin.php");
        String res = uriBuilder.getResponseFromHttpUrl(inetAddress);
        AdminModel getAdmin = new AdminModel();
        ArrayList<AdminModel> t = getAdmin.DataTransform(res);
        dlm.clear();
        for(AdminModel p : t ){
            dlm.addElement(p.getName().toString());
            //System.out.println(p.getName());
        }
        
        
        ListAdmin.setModel(dlm);
        
    }
    
    public void clearText(){
        txtID.setText("");
        txtName.setText("");
        txtPswd.setText("");
        txtPhone.setText("");
    }
    
